    private void jButtonxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonxoaActionPerformed
        // TODO add your handling code here:
        int xoa=JOptionPane.showConfirmDialog(null,"Bạn có chắc muốn xóa ?");
        if(xoa!=0)
            return;
        int i=jTable1.getSelectedRow();
        
        if(i>=0){           
            {
                KHACHHANGBUS bus=new KHACHHANGBUS();
                KHACHHANGDTO a=new KHACHHANGDTO();
                a.setMAKH(jTextFieldkhachhang.getText());
                a.setTENKH(jTextFieldtenkhachhang.getText());
                a.setDIACHI(jTextFielddiachi.getText());
                a.setSDT(jTextFieldsodienthoai.getText());
                try {
                    bus.xoa(a);
                } catch (SQLException ex) {
                    Logger.getLogger(KHACHHANG.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                model.removeRow(i);
                jTable1.setModel(model);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Vui lòng click vào bảng bên để chọn khách hàng cần xóa");
        }
    }//GEN-LAST:event_jButtonxoaActionPerformed

