    private void jButtonthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonthemActionPerformed

        // TODO add your handling code here:
        KHACHHANGDTO a=new KHACHHANGDTO();
        a.setMAKH(jTextFieldkhachhang.getText());
        a.setTENKH(jTextFieldtenkhachhang.getText());
        a.setDIACHI(jTextFielddiachi.getText());
        a.setSDT(jTextFieldsodienthoai.getText());
        KHACHHANGBUS bus=new KHACHHANGBUS();
        try {
            bus.them(a);
        } catch (SQLException ex) {
            Logger.getLogger(KHACHHANG.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(dangdocfile==1)
            return;
        
        if(KHACHHANGBUS.dskhbus==null)
            bus.doc();
        dangthongke=0;
        Vector he=new Vector();
        he.add("Mã KH");
        he.add("Tên KH");
        he.add("Địa chỉ");
        he.add("SDT");
        model=new DefaultTableModel(he,0);
        
        for(KHACHHANGDTO kh:KHACHHANGBUS.dskhbus){
            Vector r=new Vector();
            r.add(kh.getMAKH());
            r.add(kh.getTENKH());
            r.add(kh.getDIACHI());
            r.add(kh.getSDT());
            model.addRow(r);
        }
        jTable1.setModel(model);
 
    }//GEN-LAST:event_jButtonthemActionPerformed

