    private void jButtonsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsuaActionPerformed
        // TODO add your handling code here:
        NHANVIENDTO nvdto=new NHANVIENDTO();
        nvdto.setCHUCVU(jTextFieldchucvu.getText());
        nvdto.setGIOITINH(jTextFieldgioitinh.getText());
        nvdto.setLUONG(jTextFieldluong.getText());
        nvdto.setMANV(jTextFieldMANV.getText());
        nvdto.setTENNV(jTextFieldTENNV.getText());
        NHANVIENBUS nvbus=new NHANVIENBUS();
        if(nvbus.sua(nvdto)==false)
        {
            JOptionPane.showMessageDialog(null,"Sửa thất bại");
            return;
        }
        JOptionPane.showMessageDialog(null,"Sửa thành công");
        int i=jTable1.getSelectedRow();       
        tbm.setValueAt(jTextFieldMANV.getText(), i,0);
        tbm.setValueAt(jTextFieldTENNV.getText(), i,1);
        tbm.setValueAt(jTextFieldgioitinh.getText(), i,2);
        tbm.setValueAt(jTextFieldchucvu.getText(), i,3);
        tbm.setValueAt(jTextFieldluong.getText(), i,4);       
        jTable1.setModel(tbm);
        
        
        DefaultComboBoxModel mdcb=new DefaultComboBoxModel();
        Set<String> set=new HashSet<>();
        
        mdcb.addElement("Tất cả");
        for(NHANVIENDTO nv:NHANVIENBUS.dsnv)
        {
            set.add(nv.getCHUCVU());
        } 
        Iterator<String> it=set.iterator();
        while(it.hasNext()){
            mdcb.addElement(it.next());
        }
        jComboBoxchucvu.setModel(mdcb);
    }//GEN-LAST:event_jButtonsuaActionPerformed

