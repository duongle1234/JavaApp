    private void jButtontimkiemncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtontimkiemncActionPerformed
        // TODO add your handling code here:
        String ten=jTextFieldtennc.getText();
        String luongmin=jTextFieldluongmin.getText();
        String luongmax=jTextFieldluongmax.getText();
        try{
            Integer.parseInt(luongmin);
            Integer.parseInt(luongmax);
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "vui lòng nhập khoảng lương nguyên dương <=2 tỷ");
            return;
        }
        
        String chucvu=jComboBoxchucvu.getSelectedItem().toString();
            Vector header=new Vector();
                header.add("Mã nhân viên");
                header.add("Tên nhân viên");
                header.add("Giới tính");
                header.add("Chức vụ");
                header.add("Lương");
        tbm= new DefaultTableModel(header,0);
        NHANVIENBUS nvb=new NHANVIENBUS();
        ArrayList<NHANVIENDTO> nvtk=nvb.tknc(ten, luongmin, luongmax, chucvu);
        for(NHANVIENDTO nv:nvtk){
            Vector a=new Vector();
            a.add(nv.getMANV());
            a.add(nv.getTENNV());
            a.add(nv.getGIOITINH());
            a.add(nv.getCHUCVU());
            a.add(nv.getLUONG());
            tbm.addRow(a);
        }
        jTable1.setModel(tbm);
    }//GEN-LAST:event_jButtontimkiemncActionPerformed

