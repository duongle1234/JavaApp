    private void jButtonthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonthemActionPerformed
        
        NHANVIENBUS nvbus=new NHANVIENBUS();
        NHANVIENDTO nvdto=new NHANVIENDTO();               
        nvdto.setMANV(jTextFieldMANV.getText());
        nvdto.setTENNV(jTextFieldTENNV.getText());
        nvdto.setGIOITINH(jTextFieldgioitinh.getText());
        nvdto.setCHUCVU(jTextFieldchucvu.getText());
        nvdto.setLUONG(jTextFieldluong.getText());
        
        if(nvbus.them(nvdto)==true)
        {
            Vector vt=new Vector();
            vt.add(jTextFieldMANV.getText());
            vt.add(jTextFieldTENNV.getText());
            vt.add(jTextFieldgioitinh.getText());
            vt.add(jTextFieldchucvu.getText());
            vt.add(jTextFieldluong.getText());        
            tbm.addRow(vt);
            jTable1.setModel(tbm);
        }
        NHANVIENBUS bus=new NHANVIENBUS();
        DefaultComboBoxModel mdcb=new DefaultComboBoxModel();
        Set<String> set=new HashSet<>();
        mdcb.addElement("Tất cả");
        for(int i=0;i<bus.dsnv.size();i++)
            {
                NHANVIENDTO nv=NHANVIENBUS.dsnv.get(i);
                set.add(nv.getCHUCVU());
            } 
        Iterator<String> it=set.iterator();
        while(it.hasNext()){
            mdcb.addElement(it.next());
        }
        jComboBoxchucvu.setModel(mdcb);
    }//GEN-LAST:event_jButtonthemActionPerformed

